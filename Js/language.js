function translatePage() {
    document.querySelectorAll("*").forEach(element => {
        if (element.children.length === 0 && element.innerText.trim()) {
            const text = element.innerText.trim();
            if (isSpanish && translations[text]) {
                element.innerText = translations[text].es;
            } else if (!isSpanish) {
                element.innerText = translations[text] ? translations[text].en : text;
            }
        }

        if (element.hasAttribute("title")) {
            const titleText = element.getAttribute("title").trim();
            if (isSpanish && translations[titleText]) {
                element.setAttribute("title", translations[titleText].es);
            } else if (!isSpanish) {
                element.setAttribute("title", translations[titleText] ? translations[titleText].en : titleText);
            }
        }
    });
}

let isSpanish = localStorage.getItem("language") === "es";

function toggleLanguage() {
    isSpanish = !isSpanish;
    localStorage.setItem("language", isSpanish ? "es" : "en");
    translatePage();
}

document.addEventListener("DOMContentLoaded", () => {
    translatePage();
    document.getElementById("languageButton").addEventListener("click", toggleLanguage);
});
